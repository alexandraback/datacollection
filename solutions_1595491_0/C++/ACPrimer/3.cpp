#include <stdio.h>
int a[120];
int main()
{
	FILE *fout=fopen("c.txt","w+");
	int i,ncase=0;
	int T,ans,n,s,p;
	scanf("%d",&T);
	while(T--)
	{
		ans=0;
		scanf("%d%d%d",&n,&s,&p);
		fprintf(fout,"Case #%d: ",++ncase);
		for(i=0;i<n;i++) scanf("%d",&a[i]);
		for(i=0;i<n;i++)
		{
			if(a[i]%3==0)
			{
				if(a[i]/3>=p) ans++;
				else if(a[i]/3+1==p && a[i]/3-1>=0 && s>0)
				{
					ans++; s--;
				}
			}
			else if(a[i]%3==1)
			{
				if(a[i]/3+1>=p) ans++;
			}
			else if(a[i]%3==2)
			{
				if(a[i]/3+1>=p) ans++;
				else if(a[i]/3+2==p && s>0)
				{
					ans++; s--;
				}
			}
		}
		fprintf(fout,"%d\n",ans);
	}
	return 0;
}
